def Addition(iValue1,iValue2):
	iRet=iValue1 + iValue2
	return iRet
	
def Sub(iValue1,iValue2):
    iRet=iValue1 - iValue2
    return iRet
	
def MUL(iValue1,iValue2):
    iRet=iValue1 * iValue2
    return iRet
    
def Div(iValue1,iValue2):
    iRet=iValue1 / iValue2
    return iRet

    